    private void tampilId(){
       int row = tbl_buku.getRowCount();
        int col = tbl_buku.getColumnCount();
        if(row==0){
            txt_id_buku.setText("BK-1");
        } else {
        Object[] val =new Object[col];
        for (int k = 0; k < val.length - 1; k++) {
            for (int i = 0; i < row; i++) {
                for (int j = 0; j <col; j++) {
                    val[k] = tbl_buku.getValueAt(i, 0);
                    String id = val[k].toString();
                    int ambil = Integer.parseInt(id.substring(3, 4))+1;
                    txt_id_buku.setText("BK-"+String.valueOf(ambil));
                    }
            }
            }
        }
    }
