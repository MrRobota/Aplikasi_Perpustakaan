    private void tbl_bukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bukuMouseClicked
        // TODO add your handling code here:
        btn_simpan.setVisible(false);
        btn_update.setVisible(true);
        
         int row = tbl_buku.getSelectedRow();
         String Id = tbl_buku.getValueAt(row, 0).toString();
         String Kat = tbl_buku.getValueAt(row, 1).toString();
         String No = tbl_buku.getValueAt(row, 2).toString();
         String Judul = tbl_buku.getValueAt(row, 3).toString();
         String Pengarang = tbl_buku.getValueAt(row, 4).toString();
         String Penerbit = tbl_buku.getValueAt(row, 5).toString();
         String Tahun = tbl_buku.getValueAt(row, 6).toString();
         String Jumlah = tbl_buku.getValueAt(row, 7).toString();
         String Sumber = tbl_buku.getValueAt(row, 8).toString();

        txt_id_buku.setText(Id);
        txt_kat.setText(Kat);
        txt_no.setText(No);
        txt_judul.setText(Judul);
        txt_pengarang.setText(Pengarang);
        txt_penerbit.setText(Penerbit);
        txt_tahun.setText(Tahun);
        txt_jum.setText(Jumlah);
        txt_sumber.setText(Sumber);
        bk.tampilData();
    }//GEN-LAST:event_tbl_bukuMouseClicked

